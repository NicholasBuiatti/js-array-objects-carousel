function addClass (oggetto, class1, class2, class3, class4, class5) {
    oggetto.classList.add(class1)
    oggetto.classList.add(class2)
    oggetto.classList.add(class3)
    oggetto.classList.add(class4)
    oggetto.classList.add(class5)
}

function removeClass (oggetto, class1, class2, class3, class4, class5) {
    oggetto.classList.remove(class1)
    oggetto.classList.remove(class2)
    oggetto.classList.remove(class3)
    oggetto.classList.remove(class4)
    oggetto.classList.remove(class5)
}